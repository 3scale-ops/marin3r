const (
	enabledlabel string = "mariner.3scale.net/enabled"
	ownerLabel   string = "marin3r.3scale.net/owner"
)

var log = logf.Log.WithName("controller_sidecarconfig")

// Add creates a new SidecarConfig Controller and adds it to the Manager. The Manager will set fields on the Controller
// and Start it when the Manager is Started.
func Add(mgr manager.Manager) error {
	return add(mgr, newReconciler(mgr))
}

// newReconciler returns a new reconcile.Reconciler
func newReconciler(mgr manager.Manager) reconcile.Reconciler {
	return &ReconcileSidecarConfig{client: mgr.GetClient(), scheme: mgr.GetScheme()}
}

// add adds a new Controller to mgr with r as the reconcile.Reconciler
func add(mgr manager.Manager, r reconcile.Reconciler) error {
	// Create a new controller
	c, err := controller.New("sidecarconfig-controller", mgr, controller.Options{Reconciler: r})
	if err != nil {
		return err
	}

	// Watch for changes to primary resource SidecarConfig
	err = c.Watch(&source.Kind{Type: &controlplanev1alpha1.SidecarConfig{}}, &handler.EnqueueRequestForObject{})
	if err != nil {
		return err
	}

	// Watch for changes in namespace objects to ensure that namespaces
	// that contain a SidecarConfig object have the correct set of labels
	mapFn := handler.ToRequestsFunc(
		func(a handler.MapObject) []reconcile.Request {
			return []reconcile.Request{
				{NamespacedName: types.NamespacedName{
					Name:      a.Meta.GetLabels()[ownerLabel],
					Namespace: a.Meta.GetName(),
				}},
			}
		})
	filter := predicate.Funcs{
		UpdateFunc: func(e event.UpdateEvent) bool {
			// Filter out the event if the namespace does not have
			// the appropriate marin3r label
			if _, ok := e.MetaOld.GetLabels()[enabledlabel]; ok {
				return true
			}
			return false
		}}

	err = c.Watch(
		&source.Kind{Type: &corev1.Namespace{}},
		&handler.EnqueueRequestsFromMapFunc{ToRequests: mapFn},
		filter,
	)

	if err != nil {
		return err
	}

	return nil
}

